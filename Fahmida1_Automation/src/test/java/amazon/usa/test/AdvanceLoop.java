package amazon.usa.test;

public class AdvanceLoop {
	
	public static void main(String[] args) {
		// Advance loop/ Enhance loop
		int num[]= {10,20,40};    // advance loop need dimension sign and list of value inside curly bracket
		for (int Newnum:num) {    // Newnum is loop variable and num is data type variable
			System.out.println(Newnum);
		}
		 
		
	}

}
